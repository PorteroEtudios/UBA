# Operadores:

# -Aritméticos -> +, -, *, / -> valores numericos (int o float)
# -Relacionales -> ==, !=, <, >, <=, >= -> verdadero o falso (bool)
# -Lógicos -> and (y), or (o), not (no) -> verdadero o falso (bool)

# Expresión:
# Es un conjunto de operaciones relacionales por operadores logicos
# Ej:
# numero_1 = 12
# numero_2 = 45
# numero_3 = 4
# numero_4 = 76
# numero_5 = 34

# expresion 1)
# (numero_1 > numero_3) -> verdadero -> True 

# expresion 2)
# (numero_1 == numero_2) -> falso -> False

# expresion 3)
# (numero_2 < numero_4) and (numero_3 == numero_5) 
#          True         and          False        -> False

# expresion 4)
# (numero_1 + numero_5 == numero_2) or (numero_3 >= numero_4)
#              False                or          False        -> False